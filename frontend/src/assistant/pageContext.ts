function lookup(pathname: string): PageInfo | null {
  const path = pathname.replace(/\/+$/, "") || "/";
  if (PAGES[path]) return PAGES[path];
  for (const { test, info } of DYNAMIC_GUIDES) {
    if (test.test(path)) return info;
  }
  return null;
}

/** Short label for the 📍 context pill (and a fallback hint). */
export function describePageContext(pathname: string): string | null {
  return lookup(pathname)?.label ?? null;
}

/** Rich "what this page does + how to use it" briefing for the assistant. */
export function describePageGuide(pathname: string): string | null {
  return lookup(pathname)?.guide ?? null;
}
